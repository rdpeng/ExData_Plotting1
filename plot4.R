allrows <- read.table("household_power_consumption.txt", header = TRUE, sep = ";", stringsAsFactors = FALSE, na.strings = "?")
subdata <- subset(allrows, Date == "1/2/2007" | Date == "2/2/2007")
rm(allrows)
datetime <- strptime(paste(subdata$Date, subdata$Time), format = "%d/%m/%Y %H:%M:%S")
png(filename = "plot4.png", width = 480, height = 480)
par(mfrow=c(2,2))
plot(datetime, subdata$Global_active_power, type = "l", ylab = "Global Active Power (kilowatts)", xlab = "")
plot(datetime, subdata$Voltage, type = "l", ylab = "Voltage", xlab = "datetime")
plot(datetime, subdata$Sub_metering_1, type = "l", ylab = "Energy sub metering", xlab = "")
lines(datetime, subdata$Sub_metering_2, col = "red")
lines(datetime, subdata$Sub_metering_3, col = "blue")
legend("topright", c("Sub_metering_1","Sub_metering_2","Sub_metering_3"), col = c("black","red","blue"), lty = 1)
plot(datetime, subdata$Global_reactive_power, type = "l", ylab = "Global_reactive_power", xlab = "datetime")
dev.off()